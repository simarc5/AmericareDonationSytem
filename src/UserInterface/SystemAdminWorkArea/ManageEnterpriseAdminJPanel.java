    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();
        
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();
        
        if(name.isEmpty() || name.length()<4  || password.isEmpty() || password.length()<4 || username.isEmpty() || username.length()<4 )
            JOptionPane.showMessageDialog(null, "Username,password and name must be greater than 4 characters");
        
        
        
            if(!name.isEmpty() && name.length()>4  && !password.isEmpty() && password.length()>4 && !username.isEmpty() && username.length()>4 )
            {   
                
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
        boolean usernameIsUnique = enterprise.getUserAccountDirectory().checkIfUsernameIsUnique(username);
        if(usernameIsUnique){
            UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
            populateTable();
        }
            
        
        else {
            JOptionPane.showMessageDialog(null,"Username already exists");
            usernameJTextField.setText("");
            passwordJPasswordField.setText("");
            nameJTextField.setText("");
        }
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }//GEN-LAST:event_btnAddActionPerformed

