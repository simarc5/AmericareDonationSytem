    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a donation to approve.");
            return;
        }
        try {
            String approvedBy = String.valueOf(workRequestJTable.getValueAt(selectedRow, 4));
            if (approvedBy.equals("null")) {
                WorkRequest request = (WorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
                AcceptDonationWorkRequest req = (AcceptDonationWorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
                request.setSender(userAccount);
                request.setStatus("Pending");
                populateRequestTable();
                btnSend.setEnabled(true);

                //Send Email to donor
                String to = req.getDonor().getEmailid();
                String subject = "Donation accepted!";
                String message = "Thank you! Your donation is accepted. You will be notified of your pickup details within 24 hours.";

                String user = "aeddemogss@gmail.com";
                String pass = "aed_gss123";

                SendEmail.send(to, subject, message, user, pass);

            } else {
                JOptionPane.showMessageDialog(null, "Already approved.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a donation to send for collection.");
            return;
        }
        try {
            String approvedBy = String.valueOf(workRequestJTable.getValueAt(selectedRow, 4));
            AcceptDonationWorkRequest request = (AcceptDonationWorkRequest) workRequestJTable.getValueAt(selectedRow, 0);
            for (Organization o : orgs) {
                if (o instanceof WarehouseCollectionOrganization && approvedBy.equals(userAccount.getEmployee().getName())) {
                    CollectDonationWorkRequest collectionRequest = new CollectDonationWorkRequest();
                    collectionRequest.setDonationId(request.getDonor().getId());
                    collectionRequest.setDonorName(String.valueOf(workRequestJTable.getValueAt(selectedRow, 0)));
                    collectionRequest.setDonorContactNumber(String.valueOf(workRequestJTable.getValueAt(selectedRow, 2)));
                    collectionRequest.setDonorAddress(String.valueOf(workRequestJTable.getValueAt(selectedRow, 3)));
                    collectionRequest.setRequestedBy(String.valueOf(workRequestJTable.getValueAt(selectedRow, 4)));
                    collectionRequest.setStatus("Sent");
                    collectionRequest.setDonationType(request.getDonor().getDonationType());
                    collectionRequest.setDonorEmailId(request.getDonor().getEmailid());
                    o.getWorkQueue().getCollectionWorkRequestList().add(collectionRequest);
                    JOptionPane.showMessageDialog(null, "Collection request sent to Collection Warehouse.");
                    break;
                }
            }
            btnSend.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }//GEN-LAST:event_btnSendActionPerformed

