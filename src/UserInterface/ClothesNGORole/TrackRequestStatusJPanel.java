    public void populateTrackStatusJTable(){
        try{
            DefaultTableModel model = (DefaultTableModel) trackStatusJTable.getModel();
            model.setRowCount(0);
            for(CollectDonationWorkRequest request : collectionWorkRequestList){
                if(request.getDonationType().equals("Clothes")){
                   Object[] row = new Object[4];
                   row[0] = request;
                   row[1] = request.getRequestedBy();
                   row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                   row[3] = request.getStatus();
                   model.addRow(row); 
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }

