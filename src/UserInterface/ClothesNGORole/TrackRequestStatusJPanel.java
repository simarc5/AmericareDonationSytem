    private void btnTrackDistributionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTrackDistributionActionPerformed
        try{
            for(Network n : system.getNetworkList()){
                for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                    for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
                        if(o instanceof WarehouseDistributionOrganization){
                            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                            userProcessContainer.add("TrackClothesDistributionRequestJPanel", new TrackClothesDistributionRequestJPanel(userProcessContainer,userAccount,enterprise,o.getWorkQueue().getDistributionWorkRequestList()));
                            layout.next(userProcessContainer);
                            break;
                        }
                    }
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }//GEN-LAST:event_btnTrackDistributionActionPerformed

    private void btnRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestActionPerformed
        int selectedRow = trackStatusJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a request.");
        }
        try{
            String status = String.valueOf(trackStatusJTable.getValueAt(selectedRow, 3));
            if(status.equals("Complete")){
                CollectDonationWorkRequest request = (CollectDonationWorkRequest)trackStatusJTable.getValueAt(selectedRow, 0);
                if(!locationTxtBox.getText().isEmpty()){
                    DistributeDonationWorkRequest donationRequest = new DistributeDonationWorkRequest();
                    donationRequest.setDonationType(request.getDonationType());
                    donationRequest.setSender(userAccount);
                    donationRequest.setLocation(locationTxtBox.getText());
                    for(Network n : system.getNetworkList()){
                        for(Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                            for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
                                if(o instanceof WarehouseDistributionOrganization){
                                    o.getWorkQueue().getDistributionWorkRequestList().add(donationRequest);
                                    request.setStatus("Distribution Requested");
                                    populateTrackStatusJTable();
                                    JOptionPane.showMessageDialog(null, "Request sent to Warehouse Distribution.");
                                    break;
                                }
                            }
                        }
                    }
                }
                else JOptionPane.showMessageDialog(null, "Please provide location of distribution.");
            }
            else{
                JOptionPane.showMessageDialog(null, "Please select a completed request.");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something went wrong..");
        }
    }//GEN-LAST:event_btnRequestActionPerformed


